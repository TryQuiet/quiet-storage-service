import {
  SecretsManagerClient,
  GetSecretValueCommand,
} from "@aws-sdk/client-secrets-manager";

const cliOptions = process.argv
if (cliOptions.length !== 1) {
  throw new Error(`CLI options should only include the name of the secret you're reading`)
}


const secretName = cliOptions[0];
const key = 'DOTENV_PRIVATE_KEY_DEV'

const client = new SecretsManagerClient({
  region: "us-east-1",
});

let response;

try {
  response = await client.send(
    new GetSecretValueCommand({
      SecretId: secretName,
      VersionStage: "AWSCURRENT", // VersionStage defaults to AWSCURRENT if unspecified
    })
  );
} catch (error) {
  // For a list of exceptions thrown, see
  // https://docs.aws.amazon.com/secretsmanager/latest/apireference/API_GetSecretValue.html
  throw error;
}

const secret = JSON.parse(response.SecretString)[key];
process.stdout.write(secret)